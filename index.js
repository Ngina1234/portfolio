function calculator(num1, num2) {
  let num1 = 200;
  let num2 = 30;

  const sum = num1 + num2;
  const diff = num1 - num2;
  const product = num1 * num2;
  const quot = num1 / num2;

  console.log("sum: " + sum);
  console.log("difference: " + diff);
  console.log("product: " + product);
  console.log("quotient: " + quot);
}
